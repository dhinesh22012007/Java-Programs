public class IncrementDecrement {
    public static void main(String args[])
    {
        byte m=2;
        System.out.println(m++);
        System.out.println(m++);
        System.out.println(m);
        System.out.println(--m);
        System.out.println(m--);
        System.out.println(m);
    }
    
}
